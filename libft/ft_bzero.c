#include "libft.h"

void	*ft_bzero(void *s, size_t len)
{
	char		*p;
	size_t		i;

	p = (char *)s;
	i = 0;
	while (i < len)
	{
		*(p + i) = 0;
		i++;
	}
	return (s);
}
